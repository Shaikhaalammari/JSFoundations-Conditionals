// Define the numbers and the operation here
const x = 5;
const y = 3;
const operator = "/";
// Write if-statements here
if (operator === "+") {
  console.log(x + y);
} else if (operator === "-") {
  console.log(x - y);
} else if (operator === "*") {
  console.log(x * y);
} else if (operator === "/") {
  console.log(x / y);
}
